# Lists and Definite Loops

friends = ['Joeseph', 'Glenn', 'Sally']
for friends in friends:
    print('Happy New Year: ', friends)
print('done.')


# looking inside lists
friends = ['Joeseph', 'Glenn', 'Sally']
print(friends[1])

# how long is a list?
greet = 'Hello Bob'
print(len(greet))

x = [1, 2, 'joe', 99]
print(len(x))


# using the 'range' function to loop
print(range(4))

